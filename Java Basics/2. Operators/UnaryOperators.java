public class UnaryOperators {
    public static void main(String[] args) {
        int a =4;
        int b = a++;
        int c = ++a;
        int d = --a;
        int e = a--;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
